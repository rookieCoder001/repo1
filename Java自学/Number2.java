public class Number2{
	public static void main (String[] args){
		byte num2 =50;
		num2+=60;
		System.out.println(num2);//110
		short num3 = 110;
		num3 %=8;
		System.out.println(num3);//6
		int num4 = 10;
		num4++;
        System.out.println(num4);//11
		int num5= 20;
		System.out.println(++num5);//21
		System.out.println(num5++);//21
        System.out.println(num5);//22
	}
}
